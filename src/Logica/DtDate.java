package Logica;

/**
 *
 * @author ninoh
 */
public class DtDate {
    private int dia;
    private int mes;
    private int anio;
    public DtDate (int d, int m, int a){
    this.dia = d;
    this.mes = m;
    this.anio = a;
    }    

    public int getDia() {
        return dia;
    }

    public int getMes() {
        return mes;
    }

    public int getAnio() {
        return anio;
    }

    public void setDia(int dia) {
        this.dia = dia;
    }

    public void setMes(int mes) {
        this.mes = mes;
    }

    public void setAnio(int anio) {
        this.anio = anio;
    }
    
}
